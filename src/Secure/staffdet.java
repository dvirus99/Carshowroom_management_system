    private void sinsertbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sinsertbtnActionPerformed
        // TODO add your handling code here:
        conn=MySqlConnect.ConnectDB();
        String sql= "INSERT INTO STAFFDET"
                    +"(sid,sname,saddress,smobileno)"
                    +"VALUES(?,?,?,?)";
        try{
            pst=conn.prepareStatement(sql);
            pst.setString(1, sid.getText());
            pst.setString(2, sname.getText());
            pst.setString(3, saddress.getText());
            pst.setString(4, smobileno.getText());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Inserted Successfully!");
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_sinsertbtnActionPerformed

