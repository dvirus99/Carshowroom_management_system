    private void srefreshbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_srefreshbtnActionPerformed
        // TODO add your handling code here:
        conn=MySqlConnect.ConnectDB();
        String Sql="CALL DISP_STAFFDET";
        try{
            pst=conn.prepareStatement(Sql);
            rs=pst.executeQuery();
            DefaultTableModel tm=(DefaultTableModel)stable.getModel();
            tm.setRowCount(0);
            
            while(rs.next()){
                Object o[]={rs.getInt("sid"),rs.getString("sname"),rs.getString("saddress"),rs.getInt("smobileno")};
                tm.addRow(o);
                
            }
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_srefreshbtnActionPerformed

