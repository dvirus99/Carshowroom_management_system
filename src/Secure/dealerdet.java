    private void dinsertbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dinsertbtnActionPerformed
        // TODO add your handling code here:
        conn=MySqlConnect.ConnectDB();
        String sql= "INSERT INTO DEALERDET"
                    +"(did,dname,daddress,dmobileno)"
                    +"VALUES(?,?,?,?)";
        try{
            pst=conn.prepareStatement(sql);
            pst.setString(1, did.getText());
            pst.setString(2, dname.getText());
            pst.setString(3, daddress.getText());
            pst.setString(4, dmobileno.getText());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Inserted Successfully!");
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_dinsertbtnActionPerformed

