    private void drefreshbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_drefreshbtnActionPerformed
        // TODO add your handling code here:
        conn=MySqlConnect.ConnectDB();
        //String Sql="CALL DISP_DEALERDET";
        String Sql="select * from dealerdet";
        
        try{
            pst=conn.prepareStatement(Sql);
            rs=pst.executeQuery();
            DefaultTableModel tm=(DefaultTableModel)dtable.getModel();
            tm.setRowCount(0);
            
            while(rs.next()){
                Object o[]={rs.getInt("did"),rs.getString("dname"),rs.getString("daddress"),rs.getInt("dmobileno")};
                tm.addRow(o);
                
            }
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_drefreshbtnActionPerformed

